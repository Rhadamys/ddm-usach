    /**
     * Creates new form VistaMenuPrincipal
     * @param jugador Jugador que ha iniciado sesión
     */
    public VistaMenuPrincipal(Jugador jugador) {
        initComponents();
        
        this.nuevaPartida = new BotonImagen(Constantes.BTN_NUEVA_PARTIDA);
        this.nuevoTorneo = new BotonImagen(Constantes.BTN_NUEVO_TORNEO);
        this.modificarPuzzle = new BotonImagen(Constantes.BTN_MODIFICAR_PUZZLE);
        this.infoCriaturas = new BotonImagen(Constantes.BTN_INFO_CRIATURAS);
        this.salir = new BotonImagen(Constantes.BTN_SALIR);
        
        BotonImagen[] botones = {nuevaPartida, nuevoTorneo, modificarPuzzle, infoCriaturas, salir};
        
        final int ANCHO = 140;
        final int ALTO = ANCHO * 160 / 100;
        final int CANTIDAD = botones.length;
        final int SEP = (this.getWidth() - ANCHO * CANTIDAD) / (CANTIDAD + 1);
        
        String[] mensajeBoton = {"Nueva partida", "Nuevo torneo", "Modificar puzzle", "Información de criaturas", "Salir"};
        
        for (int i = 0; i < CANTIDAD; i ++){
            final int index = i;
            
            botones[i].setSize(ANCHO, ALTO);
            botones[i].setLocation((SEP + ANCHO) * i + SEP, (this.getHeight() - ALTO) / 2);
            botones[i].addMouseListener(new MouseAdapter(){
                @Override
                public void mouseEntered(MouseEvent e){
                    mensaje.setText(mensajeBoton[index]);
                }
                
                @Override
                public void mouseExited(MouseEvent e){
                    mensaje.setText("");
                }
            });
            this.add(botones[i]);
            
            PanelImagen panel = new PanelImagen(Constantes.CAJON_MENU);
            panel.setSize(ANCHO, ALTO);
            panel.setLocation((SEP + ANCHO) * i + SEP, (this.getHeight() - ALTO) / 2);
            this.add(panel);
        }
        
        this.cerrarSesion = new BotonImagen();
        this.add(cerrarSesion);
        this.cerrarSesion.setSize(60, 60);
        this.cerrarSesion.setLocation(730, 10);
        this.cerrarSesion.setImagenSobre("/Imagenes/Otros/cerrar_sesion.png");
        this.cerrarSesion.setImagenPresionado("/Imagenes/Otros/cerrar_sesion_presionado.png");
        this.cerrarSesion.setLayout(null);
        this.cerrarSesion.addMouseListener(new MouseAdapter(){
                @Override
                public void mouseEntered(MouseEvent e){
                    mensaje.setText("Cerrar sesión");
                }
                
                @Override
                public void mouseExited(MouseEvent e){
                    mensaje.setText("");
                }
            });
        
        PanelImagen iconoJugador = new PanelImagen();
        this.add(iconoJugador);
        iconoJugador.setSize(60, 60);
        iconoJugador.setLocation(730, 10);
        iconoJugador.setImagen("/Imagenes/Jefes/" + jugador.getJefeDeTerreno().getNomArchivoImagen() + ".png");
        iconoJugador.setBorder(new LineBorder(Color.darkGray, 2));
        
        this.setImagenFondo(Constantes.FONDO_MENU_PRINCIPAL);
        
        this.mensaje.setText("");
        this.mensaje.setFont(Constantes.FUENTE_36PX);
        this.mensajeBienvenida.setFont(Constantes.FUENTE_24PX);
        
        this.mensajeBienvenida.setText("<html>¡Bienvenid@ <i><b style=\"color:orange;\">" + jugador.getNombreJugador() + "</b></i>!</html>");
    }
    
